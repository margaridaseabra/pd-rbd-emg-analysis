def _bio_star_label(p):
    if pd.isna(p):
        return "n/a"
    if p < 0.001:
        return "***"
    if p < 0.01:
        return "**"
    if p < 0.05:
        return "*"
    return "ns"


def _bio_mannwhitney_p(metrics, metric, group_a, week_a, group_b, week_b):
    a = pd.to_numeric(
        metrics[
            (metrics["group"] == group_a)
            & (metrics["week"].astype(int) == int(week_a))
        ][metric],
        errors="coerce",
    ).dropna()

    b = pd.to_numeric(
        metrics[
            (metrics["group"] == group_b)
            & (metrics["week"].astype(int) == int(week_b))
        ][metric],
        errors="coerce",
    ).dropna()

    if len(a) < 2 or len(b) < 2:
        return np.nan

    try:
        return float(stats.mannwhitneyu(a, b, alternative="two-sided").pvalue)
    except Exception:
        return np.nan


