def _find_child_text(node, names, default=""):
    for name in names:
        found = node.find(name)
        if found is not None and found.text is not None:
            return found.text.strip()
    return default


def _parse_datetime_safe(x):
    try:
        return datetime.fromisoformat(str(x).strip())
    except Exception:
        return None


def parse_exp_video_mapping_from_text(exp_text):
    """
    Parse .exp XML and return one row per acquisition/video segment.

    Assumption:
    acquisition file index 0 corresponds to video file index 0,
    acquisition file index 1 corresponds to video file index 1, etc.
    """
    root = ET.fromstring(exp_text)

    acq_files = []
    video_files = []

    # Acquisition files
    acq_file_nodes = root.findall(".//Acquisition//Files//File")
    if not acq_file_nodes:
        acq_file_nodes = root.findall(".//Acquisition//File")

    for f in acq_file_nodes:
        fname = _find_child_text(f, ["FileName", "Filename", "Name"])
        tstart = _find_child_text(f, ["TStart", "StartTime", "Start"])
        duration = _find_child_text(f, ["Duration", "Length"])

        if fname:
            acq_files.append(
                {
                    "acq_file_name": fname,
                    "acq_tstart": tstart,
                    "acq_duration_raw": duration,
                }
            )

    # Video files
    video_nodes = root.findall(".//Videos//Video")
    if not video_nodes:
        video_nodes = root.findall(".//Video")

    for video_node in video_nodes:
        video_name = _find_child_text(video_node, ["Name"], default="video")
        video_sampling_rate = _find_child_text(video_node, ["SamplingRate", "FrameRate"])

        file_nodes = video_node.findall(".//Files//File")
        if not file_nodes:
            file_nodes = video_node.findall(".//File")

        for f in file_nodes:
            fname = _find_child_text(f, ["FileName", "Filename", "Name"])
            tstart = _find_child_text(f, ["TStart", "StartTime", "Start"])
            duration = _find_child_text(f, ["Duration", "Length"])

            if fname:
                video_files.append(
                    {
                        "video_stream_name": video_name,
                        "video_sampling_rate": video_sampling_rate,
                        "video_file_name": fname,
                        "video_tstart": tstart,
                        "video_duration_raw": duration,
                    }
                )

    rows = []
    n = max(len(acq_files), len(video_files))

    for i in range(n):
        acq = acq_files[i] if i < len(acq_files) else {}
        vid = video_files[i] if i < len(video_files) else {}

        acq_dt = _parse_datetime_safe(acq.get("acq_tstart", ""))
        vid_dt = _parse_datetime_safe(vid.get("video_tstart", ""))

        video_time_offset_s = 0.0
        if acq_dt is not None and vid_dt is not None:
            # video_time = signal_time + offset
            # If video starts after acquisition, offset is negative.
            video_time_offset_s = (acq_dt - vid_dt).total_seconds()

        rows.append(
            {
                "segment_id": i,
                **acq,
                **vid,
                "video_time_offset_s": video_time_offset_s,
            }
        )

    return pd.DataFrame(rows)


def parse_exp_video_mapping_from_path(exp_path):
    exp_path = Path(exp_path)
    text = exp_path.read_text(errors="ignore")
    return parse_exp_video_mapping_from_text(text)


def find_video_file_by_name(video_file_name, video_root):
    video_root = Path(video_root)

    if not video_root.exists() or not video_file_name:
        return None

    matches = list(video_root.rglob(video_file_name))
    if matches:
        return str(matches[0])

    # Try a looser search if exact filename fails.
    compact_name = compact_text(video_file_name)
    for p in index_video_files(str(video_root)):
        if compact_name in compact_text(Path(p).name):
            return str(p)

    return None


def get_video_for_event_from_exp(row, exp_map, video_root):
    seg_id = int(row["segment_id"])

    match = exp_map[exp_map["segment_id"].astype(int) == seg_id]

    if len(match) == 0:
        return None, None

    m = match.iloc[0]

    video_name = m.get("video_file_name", "")
    video_offset = float(m.get("video_time_offset_s", 0.0))

    video_path = find_video_file_by_name(video_name, video_root)

    return video_path, video_offset



def get_prob_for_state(probs, state_names, state):
    """
    Return probability curve for a normalized state name.
    This keeps Wake/Awake naming consistent.
    """
    norm_names = [normalize_state(s) for s in state_names]

    if state not in norm_names:
        return None

    idx = norm_names.index(state)
    return probs[:, idx]


