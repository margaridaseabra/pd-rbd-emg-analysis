def _get_group_values(metrics, metric, group, week):
    sub = metrics[(metrics["group"] == group) & (metrics["week"].astype(int) == int(week))]
    vals = pd.to_numeric(sub[metric], errors="coerce").dropna().values.astype(float)
    return vals


def _safe_mannwhitney(x, y):
    if len(x) < 2 or len(y) < 2:
        return np.nan
    try:
        return float(stats.mannwhitneyu(x, y, alternative="two-sided").pvalue)
    except Exception:
        return np.nan


def _safe_kruskal(groups):
    valid = [g for g in groups if len(g) >= 2]
    if len(valid) < 2:
        return np.nan
    try:
        return float(stats.kruskal(*valid).pvalue)
    except Exception:
        return np.nan


