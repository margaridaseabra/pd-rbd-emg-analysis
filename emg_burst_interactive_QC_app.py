def live_load_qc_annotations():
    """
    Do not cache this. We want the dashboard to update as QC notes are saved.
    """
    if QC_OUT.exists():
        qc = pd.read_csv(QC_OUT)
        if "qc_event_id" in qc.columns:
            qc = qc.drop_duplicates("qc_event_id", keep="last")
        return qc
    return pd.DataFrame(columns=["qc_event_id", "qc_status", "qc_notes"])


def add_live_qc_to_events(events):
    out = events.copy()

    # Remove stale QC columns if they were already merged.
    for col in ["qc_status", "qc_notes"]:
        if col in out.columns:
            out = out.drop(columns=[col])

    qc = live_load_qc_annotations()

    out = ensure_event_keys(out)

    if len(qc):
        # Prefer stable_event_key matching. Fall back to qc_event_id for older annotations.
        if "stable_event_key" in qc.columns:
            keep_cols = [c for c in ["stable_event_key", "qc_status", "qc_notes"] if c in qc.columns]
            out = out.merge(qc[keep_cols].drop_duplicates("stable_event_key", keep="last"), on="stable_event_key", how="left")
        else:
            keep_cols = [c for c in ["qc_event_id", "qc_status", "qc_notes"] if c in qc.columns]
            out = out.merge(qc[keep_cols].drop_duplicates("qc_event_id", keep="last"), on="qc_event_id", how="left")
    else:
        out["qc_status"] = "not_reviewed"
        out["qc_notes"] = ""

    out["qc_status"] = out["qc_status"].fillna("not_reviewed")
    out["qc_notes"] = out["qc_notes"].fillna("")

    return out


def _safe_div_num(a, b):
    try:
        if b is None or b == 0 or not np.isfinite(b):
            return np.nan
        return a / b
    except Exception:
        return np.nan


def load_eegonly_prediction_for_metrics(prob_path):
    z = np.load(prob_path, allow_pickle=True)
    raw_states = list(z.files)
    states = [normalize_state(s) for s in raw_states]
    probs = np.vstack([np.asarray(z[s], dtype=float) for s in raw_states]).T
    pred = np.array(states)[np.argmax(probs, axis=1)]
    return pred, probs, states


@st.cache_data(show_spinner=False)
def cached_rem_architecture(prob_path):
    pred, probs, states = load_eegonly_prediction_for_metrics(prob_path)

    n = len(pred)
    recording_min = n * EPOCH_SEC / 60

    rem = pred == "REM"
    n_rem_epochs = int(rem.sum())
    total_rem_min = n_rem_epochs * EPOCH_SEC / 60

    if n > 1:
        transitions = np.where(pred[1:] != pred[:-1])[0] + 1
    else:
        transitions = np.array([], dtype=int)

    if len(transitions):
        idx = np.arange(n)
        dist = np.full(n, np.inf)
        for t in transitions:
            dist = np.minimum(dist, np.abs(idx - t))
    else:
        dist = np.full(n, np.inf)

    stable_rem = rem & ((dist * EPOCH_SEC) >= 30)
    stable_rem_min = stable_rem.sum() * EPOCH_SEC / 60

    starts = np.where(rem & np.r_[True, ~rem[:-1]])[0]
    ends = np.where(rem & np.r_[~rem[1:], True])[0]
    bout_durs_sec = (ends - starts + 1) * EPOCH_SEC if len(starts) else np.array([])

    return {
        "n_epochs": n,
        "recording_min": recording_min,
        "n_REM_epochs": n_rem_epochs,
        "total_EEGonly_REM_min": total_rem_min,
        "pct_EEGonly_REM": _safe_div_num(100 * total_rem_min, recording_min),
        "stable_EEGonly_REM_min": stable_rem_min,
        "n_REM_bouts": int(len(bout_durs_sec)),
        "mean_REM_bout_duration_sec": float(np.mean(bout_durs_sec)) if len(bout_durs_sec) else np.nan,
        "median_REM_bout_duration_sec": float(np.median(bout_durs_sec)) if len(bout_durs_sec) else np.nan,
        "short_REM_bouts_lt30s": int(np.sum(bout_durs_sec < 30)) if len(bout_durs_sec) else 0,
        "REM_fragmentation_index_bouts_per_REM_min": _safe_div_num(len(bout_durs_sec), total_rem_min),
        "rem_epoch_mask": rem,
    }


def get_segment_probability_path(row):
    candidates = [
        "file_path_state_probabilities_EEGonly",
        "file_path_state_probabilities_EEG",
        "file_path_state_probabilities",
    ]
    for col in candidates:
        if col in row.index and isinstance(row[col], str) and Path(row[col]).exists():
            return row[col]
    return ""





def load_master_segments_for_metrics(events):
    """
    Return a segment-level table used as the denominator/master list.

    This is important because the event table only contains detected EMG episodes.
    A mouse with zero detected episodes, such as Mouse 12 W21, would otherwise
    disappear from mouse-level plots. Therefore, the EEG-only application manifest
    is the master list for REM denominators and zero-event mice.
    """
    seg_cols = ["recording_name", "group", "week", "mouse_id", "segment_id"]

    if EEG_APP_MANIFEST.exists():
        master = pd.read_csv(EEG_APP_MANIFEST)

        needed = seg_cols + [
            "file_path_state_probabilities",
            "file_path_raw_signals",
            "file_path_manual_state_annotation",
        ]
        needed = [c for c in needed if c in master.columns]

        master = master[needed].drop_duplicates().copy()

        if "file_path_state_probabilities" in master.columns:
            master = master.rename(
                columns={"file_path_state_probabilities": "file_path_state_probabilities_EEGonly"}
            )

        return master.drop_duplicates()

    # Fallback: old behavior, event-derived segments only.
    return events[seg_cols].drop_duplicates().copy()



def compute_mouse_level_dashboard_metrics(events_in):
    """
    One row per group/week/mouse. This avoids event-level pseudoreplication.
    """
    events = add_live_qc_to_events(events_in)
    events = add_rem_wake_dissociation_score(events)
    events = merge_unsupervised_morphology_to_events(events)
    events = add_rem_focused_unsupervised_flags(events)

    required = ["recording_name", "group", "week", "mouse_id", "segment_id"]
    for col in required:
        if col not in events.columns:
            raise ValueError(f"Missing required column for metrics: {col}")

    # Standardize useful columns.
    if "primary_category" not in events.columns and "event_class" in events.columns:
        events["primary_category"] = events["event_class"]

    if "duration_sec_for_category" not in events.columns:
        if "duration_sec" in events.columns:
            events["duration_sec_for_category"] = events["duration_sec"]
        else:
            events["duration_sec_for_category"] = events["end_sec"] - events["start_sec"]

    events["REM_relevant_auto"] = events["primary_category"].isin(REM_RELEVANT_CATEGORIES)
    events["primary_RBDlike_auto"] = events["primary_category"].isin(PRIMARY_RBDLIKE_CATEGORIES)
    events["QC_possible_RBD_like"] = events["qc_status"].isin(QC_POSITIVE_STATUSES)
    events["QC_transition_event"] = events["qc_status"].isin(QC_TRANSITION_STATUSES)
    events["QC_possible_or_transition"] = events["qc_status"].isin(QC_BROAD_POSITIVE_STATUSES)
    events["QC_excluded_or_artifact"] = events["qc_status"].isin(QC_EXCLUDE_STATUSES)
    events["QC_real_burst_but_wake"] = events["qc_status"].isin(QC_WAKE_STATUSES)
    events["QC_uncertain"] = events["qc_status"].isin(QC_UNCERTAIN_STATUSES)
    events["QC_reviewed"] = events["qc_status"].ne("not_reviewed")

    if "distance_to_transition_sec" in events.columns:
        events["near_transition_30s"] = events["distance_to_transition_sec"] < 30
    elif "min_EEGonly_distance_to_transition_sec" in events.columns:
        events["near_transition_30s"] = events["min_EEGonly_distance_to_transition_sec"] < 30
    else:
        events["near_transition_30s"] = False

    # Segment-level REM architecture and REM-epoch EMG coverage.
    seg_rows = []

    seg_cols = ["recording_name", "group", "week", "mouse_id", "segment_id"]
    segments = events[seg_cols].drop_duplicates().copy()

    for _, seg in segments.iterrows():
        sub = events[
            (events["recording_name"] == seg["recording_name"])
            & (events["group"] == seg["group"])
            & (events["week"] == seg["week"])
            & (events["mouse_id"] == seg["mouse_id"])
            & (events["segment_id"] == seg["segment_id"])
        ].copy()

        prob_path = get_segment_probability_path(sub.iloc[0])
        if not prob_path:
            continue

        try:
            arch = cached_rem_architecture(prob_path)
        except Exception:
            continue

        rem_mask = arch["rem_epoch_mask"]
        n_epochs = len(rem_mask)

        any_event_epochs = np.zeros(n_epochs, dtype=bool)
        rem_relevant_event_epochs = np.zeros(n_epochs, dtype=bool)
        primary_rbd_event_epochs = np.zeros(n_epochs, dtype=bool)
        qc_possible_event_epochs = np.zeros(n_epochs, dtype=bool)

        for _, ev in sub.iterrows():
            e0 = int(np.floor(float(ev["start_sec"]) / EPOCH_SEC))
            e1 = int(np.ceil(float(ev["end_sec"]) / EPOCH_SEC))
            e0 = max(0, min(n_epochs, e0))
            e1 = max(e0 + 1, min(n_epochs, e1))

            any_event_epochs[e0:e1] = True

            if bool(ev["REM_relevant_auto"]):
                rem_relevant_event_epochs[e0:e1] = True

            if bool(ev["primary_RBDlike_auto"]):
                primary_rbd_event_epochs[e0:e1] = True

            if bool(ev["QC_possible_RBD_like"]):
                qc_possible_event_epochs[e0:e1] = True

        n_rem = int(rem_mask.sum())

        seg_rows.append({
            "recording_name": seg["recording_name"],
            "group": seg["group"],
            "week": int(seg["week"]),
            "mouse_id": int(seg["mouse_id"]),
            "segment_id": int(seg["segment_id"]),
            "recording_min": arch["recording_min"],
            "n_REM_epochs": arch["n_REM_epochs"],
            "total_EEGonly_REM_min": arch["total_EEGonly_REM_min"],
            "pct_EEGonly_REM": arch["pct_EEGonly_REM"],
            "stable_EEGonly_REM_min": arch["stable_EEGonly_REM_min"],
            "n_REM_bouts": arch["n_REM_bouts"],
            "mean_REM_bout_duration_sec": arch["mean_REM_bout_duration_sec"],
            "median_REM_bout_duration_sec": arch["median_REM_bout_duration_sec"],
            "short_REM_bouts_lt30s": arch["short_REM_bouts_lt30s"],
            "REM_fragmentation_index_bouts_per_REM_min": arch["REM_fragmentation_index_bouts_per_REM_min"],
            "REM_epochs_with_any_EMG_event": int((rem_mask & any_event_epochs).sum()),
            "REM_epochs_with_REM_relevant_EMG_event": int((rem_mask & rem_relevant_event_epochs).sum()),
            "REM_epochs_with_primary_RBDlike_event": int((rem_mask & primary_rbd_event_epochs).sum()),
            "REM_epochs_with_QC_possible_RBD_like": int((rem_mask & qc_possible_event_epochs).sum()),
        })

    seg_metrics = pd.DataFrame(seg_rows)

    if len(seg_metrics) == 0:
        return pd.DataFrame(), pd.DataFrame(), events

    # Mouse-level REM architecture.
    mouse_rem = seg_metrics.groupby(["group", "week", "mouse_id"], as_index=False).agg(
        recording_min=("recording_min", "sum"),
        n_REM_epochs=("n_REM_epochs", "sum"),
        total_EEGonly_REM_min=("total_EEGonly_REM_min", "sum"),
        stable_EEGonly_REM_min=("stable_EEGonly_REM_min", "sum"),
        n_REM_bouts=("n_REM_bouts", "sum"),
        short_REM_bouts_lt30s=("short_REM_bouts_lt30s", "sum"),
        REM_epochs_with_any_EMG_event=("REM_epochs_with_any_EMG_event", "sum"),
        REM_epochs_with_REM_relevant_EMG_event=("REM_epochs_with_REM_relevant_EMG_event", "sum"),
        REM_epochs_with_primary_RBDlike_event=("REM_epochs_with_primary_RBDlike_event", "sum"),
        REM_epochs_with_QC_possible_RBD_like=("REM_epochs_with_QC_possible_RBD_like", "sum"),
    )

