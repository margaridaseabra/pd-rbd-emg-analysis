def p_to_stars(p):
    if pd.isna(p):
        return "n/a"
    if p < 0.001:
        return "***"
    if p < 0.01:
        return "**"
    if p < 0.05:
        return "*"
    return "ns"


def _mw_p_for_plot(metrics, metric, a, b):
    x = metrics[
        (metrics["group"] == a[0]) & (metrics["week"].astype(int) == int(a[1]))
    ][metric]
    y = metrics[
        (metrics["group"] == b[0]) & (metrics["week"].astype(int) == int(b[1]))
    ][metric]

    x = pd.to_numeric(x, errors="coerce").dropna().values.astype(float)
    y = pd.to_numeric(y, errors="coerce").dropna().values.astype(float)

    if len(x) < 2 or len(y) < 2:
        return np.nan

    try:
        return float(stats.mannwhitneyu(x, y, alternative="two-sided").pvalue)
    except Exception:
        return np.nan


def add_emg_morphology_class(df):
    """
    Adds an automatic morphology label without changing primary_category or qc_status.

    This is intentionally descriptive, not diagnostic:
    - phasic_twitch_like: short, isolated episode
    - clustered_phasic: several microbursts grouped close together
    - sustained_EMG_episode: longer/highly occupied EMG activation
    - long_complex_episode: long episode but not fully sustained
    - unclassified_morphology: insufficient information
    """
    out = df.copy()

    if "duration_sec_for_category" in out.columns:
        dur = pd.to_numeric(out["duration_sec_for_category"], errors="coerce")
    elif "duration_sec" in out.columns:
        dur = pd.to_numeric(out["duration_sec"], errors="coerce")
    else:
        dur = pd.to_numeric(out["end_sec"], errors="coerce") - pd.to_numeric(out["start_sec"], errors="coerce")

    n_micro = pd.to_numeric(out.get("n_microbursts_in_episode", np.nan), errors="coerce")
    micro_frac = pd.to_numeric(out.get("microburst_fraction_in_episode", np.nan), errors="coerce")
    total_micro = pd.to_numeric(out.get("total_microburst_duration_sec", np.nan), errors="coerce")

    morph = pd.Series("unclassified_morphology", index=out.index, dtype="object")

    # Conservative morphology rules.
    phasic = (dur <= 2.0) & ((n_micro.isna()) | (n_micro <= 2))
    clustered = (dur > 2.0) & (dur <= 10.0) & (n_micro >= 2)
    sustained = (dur > 10.0) | (micro_frac >= 0.70) | (total_micro >= 5.0)
    long_complex = (dur > 10.0) & ~sustained

    morph.loc[phasic] = "phasic_twitch_like"
    morph.loc[clustered] = "clustered_phasic"
    morph.loc[long_complex] = "long_complex_episode"
    morph.loc[sustained] = "sustained_EMG_episode"

    out["emg_morphology_class"] = morph

    return out


