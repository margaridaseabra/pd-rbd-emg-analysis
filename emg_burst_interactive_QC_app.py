def cached_plot_path_for_event(row, window_min, fmax, show_manual):
    """
    Disk cache path for event QC plot.
    Uses stable_event_key when available, otherwise falls back to qc_event_id.
    """
    if "stable_event_key" in row.index and pd.notna(row.get("stable_event_key")):
        key = str(row.get("stable_event_key"))
    else:
        key = f"event_{int(row['qc_event_id'])}"

    manual_tag = "manual" if show_manual else "nomanual"
    fname = f"{key}_win{float(window_min):.1f}_fmax{int(fmax)}_{manual_tag}.png"
    return QC_PLOT_CACHE / fname


def render_cached_event_plot(row, window_min, fmax, show_manual, force_regenerate=False):
    """
    Display cached event plot if available.
    Otherwise generate it once, save to disk, and display the saved PNG.
    """
    out_path = cached_plot_path_for_event(row, window_min, fmax, show_manual)

    if out_path.exists() and out_path.stat().st_size > 0 and not force_regenerate:
        st.image(str(out_path), use_container_width=True)
        st.caption(f"Loaded cached plot: {out_path.name}")
        return

    with st.spinner("Generating signal/scoring/spectrogram plot..."):
        fig = plot_event(
            row,
            window_min=window_min,
            fmax=fmax,
            show_manual=show_manual,
        )
        fig.savefig(out_path, dpi=150, bbox_inches="tight")
        plt.close(fig)

    st.image(str(out_path), use_container_width=True)
    st.caption(f"Generated and cached plot: {out_path.name}")


