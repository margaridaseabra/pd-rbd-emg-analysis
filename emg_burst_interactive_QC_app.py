    metrics["n_QC_reviewed_primary_RBDlike"] = metrics["n_QC_reviewed_primary_RBDlike"].fillna(0)

    metrics["QC_review_coverage_primary_RBDlike_pct"] = (
        100 * metrics["n_QC_reviewed_primary_RBDlike"] / metrics["n_primary_RBDlike_auto"].replace(0, np.nan)
    )

    # Category fractions.
    for cat in REM_RELEVANT_CATEGORIES + ["wake_like_movement", "NREM_like_EMG", "other_uncertain"]:
        if cat in metrics.columns:
            metrics[f"fraction_{cat}"] = metrics[cat] / metrics["n_EMG_episodes_total"].replace(0, np.nan)

    return metrics, seg_metrics, events


