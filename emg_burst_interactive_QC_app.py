@st.cache_data
def index_video_files(video_root):
    video_root = Path(video_root)

    if not video_root.exists():
        return []

    files = []
    for ext in VIDEO_EXTENSIONS:
        files.extend(video_root.rglob(f"*{ext}"))
        files.extend(video_root.rglob(f"*{ext.upper()}"))

    return sorted([str(p) for p in files])


def compact_text(x):
    return re.sub(r"[^a-zA-Z0-9]+", "", str(x)).lower()


def video_match_score(row, video_path):
    p = Path(video_path)
    text = compact_text(str(p))

    score = 0

    rec = compact_text(row.get("recording_name", ""))
    if rec and rec in text:
        score += 100

    mouse_id = row.get("mouse_id", "")
    try:
        mouse_id_int = int(mouse_id)
        tokens = [
            f"mouse{mouse_id_int}",
            f"m{mouse_id_int}",
            f"Mouse{mouse_id_int}",
            f"M{mouse_id_int}",
        ]
        for tok in tokens:
            if compact_text(tok) in text:
                score += 20
    except Exception:
        pass

    rec_raw = str(row.get("recording_name", ""))
    dates = re.findall(r"20\d{6}", rec_raw)
    for d in dates:
        if d in text:
            score += 30

    segment_id = row.get("segment_id", "")
    try:
        seg = int(segment_id)
        seg_tokens = [f"segment{seg}", f"segment{seg:02d}", f"seg{seg}", f"seg{seg:02d}"]
        for tok in seg_tokens:
            if compact_text(tok) in text:
                score += 5
    except Exception:
        pass

    return score


def find_candidate_videos(row, video_root, max_results=20):
    video_files = index_video_files(video_root)

    scored = []
    for f in video_files:
        score = video_match_score(row, f)
        if score > 0:
            scored.append((score, f))

    scored = sorted(scored, key=lambda x: x[0], reverse=True)
    return [f for score, f in scored[:max_results]]


def make_video_clip(
    video_path,
    event_start_s,
    event_end_s,
    pre_s=10,
    post_s=10,
    video_time_offset_s=0.0,
):
    video_path = Path(video_path)

    clip_start_s = max(
        0.0,
        float(event_start_s) + float(video_time_offset_s) - float(pre_s),
    )
    clip_end_s = max(
        clip_start_s + 0.5,
        float(event_end_s) + float(video_time_offset_s) + float(post_s),
    )
    clip_duration_s = clip_end_s - clip_start_s

    key = f"{video_path}|{clip_start_s:.3f}|{clip_duration_s:.3f}"
    digest = hashlib.md5(key.encode()).hexdigest()[:12]

    out_path = VIDEO_CLIP_CACHE / f"event_video_clip_{digest}.mp4"

    if out_path.exists() and out_path.stat().st_size > 0:
        return out_path, clip_start_s, clip_duration_s

    cmd = [
        "ffmpeg",
        "-y",
        "-ss",
        f"{clip_start_s:.3f}",
        "-i",
        str(video_path),
        "-t",
        f"{clip_duration_s:.3f}",
        "-an",
        "-vf",
        "scale='min(900,iw)':-2",
        "-vcodec",
        "libx264",
        "-pix_fmt",
        "yuv420p",
        "-movflags",
        "+faststart",
        str(out_path),
    ]

    subprocess.run(cmd, check=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    return out_path, clip_start_s, clip_duration_s


