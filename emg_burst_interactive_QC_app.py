def repair_qc_event_id_after_merges(df):
    """
    Robustly restore qc_event_id after merges.

    Some merges can create qc_event_id_x / qc_event_id_y.
    The app expects a plain qc_event_id column.
    """
    out = df.copy()

    if "qc_event_id" not in out.columns:
        candidates = [
            "qc_event_id_x",
            "qc_event_id_event",
            "qc_event_id_original",
            "qc_event_id_y",
        ]

        for c in candidates:
            if c in out.columns:
                out["qc_event_id"] = out[c]
                break

    # Drop duplicate merge suffix versions after recovering.
    for c in ["qc_event_id_x", "qc_event_id_y", "qc_event_id_event", "qc_event_id_original"]:
        if c in out.columns and c != "qc_event_id":
            out = out.drop(columns=[c])

    if "qc_event_id" in out.columns:
        out["qc_event_id"] = pd.to_numeric(out["qc_event_id"], errors="coerce")

    return out


def _auto_add_unsupervised_metaclass_labels(df):
    """
    Add fine/broad biological labels from GMM cluster ids if they are missing.
    This lets the app work even if the separate metaclass CSV was not created.
    """
    out = df.copy()

    if "emg_morphology_cluster" not in out.columns:
        return out

    fine_map = {
        0: "ultra_brief_low_amplitude_or_mixed",
        1: "brief_phasic_twitch_like",
        2: "sustained_moderate_complex",
        3: "brief_high_duty_phasic",
        4: "clustered_phasic_high_background_tone",
        5: "sustained_high_amplitude_tonic_like",
    }

    broad_map = {
        0: "phasic_or_uncertain",
        1: "phasic",
        2: "sustained_tonic",
        3: "phasic",
        4: "clustered_phasic_high_tone",
        5: "sustained_tonic",
    }

    cluster_numeric = pd.to_numeric(out["emg_morphology_cluster"], errors="coerce").astype("Int64")

    if "emg_morphology_fine_label" not in out.columns:
        out["emg_morphology_fine_label"] = cluster_numeric.map(fine_map)

    if "emg_morphology_broad_label" not in out.columns:
        out["emg_morphology_broad_label"] = cluster_numeric.map(broad_map)

    return out


@st.cache_data(show_spinner=False)
def load_unsupervised_morphology_table(path):
    path = Path(path)

    if not path.exists():
        fallback = DEFAULT_UNSUPERVISED_MORPHOLOGY_FALLBACK
        if fallback.exists():
            path = fallback
        else:
            return pd.DataFrame()

    morph = pd.read_csv(path)
    morph = _auto_add_unsupervised_metaclass_labels(morph)

    return morph


def merge_unsupervised_morphology_to_events(events, morphology_path=None):
    """
    Merge refined unsupervised EMG morphology clusters into the event table.

    Preferred key:
        stable_event_key

    Fallback:
        qc_event_id
    """
    out = events.copy()

    if morphology_path is None:
        morphology_path = DEFAULT_UNSUPERVISED_MORPHOLOGY

    morph = load_unsupervised_morphology_table(str(morphology_path))

    if len(morph) == 0:
        out["unsupervised_morphology_available"] = False
        return out

    morph_cols = [
        "stable_event_key",
        "qc_event_id",
        "emg_morphology_cluster",
        "emg_morphology_cluster_probability",
        "suggested_morphology_label",
        "emg_morphology_fine_label",
        "emg_morphology_broad_label",
        "pca1",
        "pca2",
        "morph_duration_sec",
        "emg_rms_z_max",
        "emg_rms_z_mean",
        "emg_rms_z_p95",
        "active_fraction_in_event",
        "n_subbursts",
        "rms_peak_to_mean_ratio",
        "background_rms_median",
        "pre_event_rms_median",
        "post_event_rms_median",
        "event_density_per_segment_hour",
        "local_event_count_pm60s",
        "local_event_count_pm300s",
    ]

    morph_cols = [c for c in morph_cols if c in morph.columns]
    morph = morph[morph_cols].copy()

    # Avoid duplicate stale columns in the app table.
    cols_to_refresh = [
        c for c in morph_cols
        if c not in ["stable_event_key", "qc_event_id"] and c in out.columns
    ]
    if cols_to_refresh:
        out = out.drop(columns=cols_to_refresh)

    if (
        "stable_event_key" in out.columns
        and "stable_event_key" in morph.columns
        and morph["stable_event_key"].notna().any()
    ):
        # Important: when merging by stable_event_key, do not also bring
        # qc_event_id from the morphology table, otherwise pandas creates
        # qc_event_id_x / qc_event_id_y and the app later crashes.
        merge_cols = [c for c in morph.columns if c != "qc_event_id"]
        morph = morph[merge_cols].drop_duplicates("stable_event_key", keep="last")
        out = out.merge(morph, on="stable_event_key", how="left")

    elif "qc_event_id" in out.columns and "qc_event_id" in morph.columns:
        morph = morph.drop_duplicates("qc_event_id", keep="last")
        out = out.merge(morph, on="qc_event_id", how="left")

    else:
        out["unsupervised_morphology_available"] = False
        return out

    out["unsupervised_morphology_available"] = out.get(
        "emg_morphology_cluster",
        pd.Series(index=out.index, dtype=float),
    ).notna()

    out = repair_qc_event_id_after_merges(out)

    return out


def add_rem_focused_unsupervised_flags(events):
    """
    Add boolean flags defining REM-focused subsets for unsupervised analysis.
    """
    out = events.copy()

    if "primary_category" not in out.columns and "event_class" in out.columns:
        out["primary_category"] = out["event_class"]

    if "primary_category" not in out.columns:
        out["primary_category"] = ""

    rem_relevant_categories = [
        "stable_REM_EMG_burst",
        "EMG_suppressed_REM",
        "mixed_REM_Wake_transition",
    ]

    out["unsup_strict_stable_REM"] = out["primary_category"].eq("stable_REM_EMG_burst")
    out["unsup_REM_relevant_auto"] = out["primary_category"].isin(rem_relevant_categories)

    if "EEGonly_state_center" in out.columns:
        out["unsup_EEGonly_center_REM"] = out["EEGonly_state_center"].map(normalize_state).eq("REM")
    else:
        out["unsup_EEGonly_center_REM"] = False

    if "P_REM_EEGonly" in out.columns:
        out["unsup_EEGonly_PREM_ge_060"] = pd.to_numeric(
            out["P_REM_EEGonly"],
            errors="coerce",
        ).ge(0.60)
    else:
        out["unsup_EEGonly_PREM_ge_060"] = False

    if "candidate_rbd_like_dissociation" in out.columns:
        out["unsup_dissociation_positive"] = out["candidate_rbd_like_dissociation"].fillna(False).astype(bool)
    elif "rbd_dissociation_score" in out.columns:
        out["unsup_dissociation_positive"] = pd.to_numeric(
            out["rbd_dissociation_score"],
            errors="coerce",
        ).gt(0)
    else:
        out["unsup_dissociation_positive"] = False

    out["unsup_REM_like_union"] = (
        out["unsup_strict_stable_REM"]
        | out["unsup_REM_relevant_auto"]
        | out["unsup_EEGonly_center_REM"]
        | out["unsup_EEGonly_PREM_ge_060"]
        | out["unsup_dissociation_positive"]
    )

    return out


def apply_unsupervised_rem_subset_filter(events, subset_name):
    out = events.copy()

    if subset_name == "All events":
        return out

    if subset_name == "Strict stable REM EMG bursts":
        return out[out.get("unsup_strict_stable_REM", False)].copy()

    if subset_name == "REM-relevant automatic categories":
        return out[out.get("unsup_REM_relevant_auto", False)].copy()

    if subset_name == "EEG-only center REM":
        return out[out.get("unsup_EEGonly_center_REM", False)].copy()

    if subset_name == "EEG-only P_REM >= 0.60":
        return out[out.get("unsup_EEGonly_PREM_ge_060", False)].copy()

    if subset_name == "Dissociation-positive candidates":
        return out[out.get("unsup_dissociation_positive", False)].copy()

    if subset_name == "REM-like union":
        return out[out.get("unsup_REM_like_union", False)].copy()

    return out


def _percentage_table_from_crosstab(tab, axis="index"):
    if len(tab) == 0:
        return tab

    if axis == "index":
        denom = tab.sum(axis=1).replace(0, np.nan)
        return tab.div(denom, axis=0) * 100

    denom = tab.sum(axis=0).replace(0, np.nan)
    return tab.div(denom, axis=1) * 100


