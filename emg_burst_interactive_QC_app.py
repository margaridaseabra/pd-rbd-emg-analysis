DISSOCIATION_REM_EEG_THRESHOLD = 0.60
DISSOCIATION_DELTA_THRESHOLD = 0.30
DISSOCIATION_EMG_Z_THRESHOLD = 2.0


def _first_existing_metric_column(df, candidates):
    for col in candidates:
        if col in df.columns:
            return col
    return None


def add_rem_wake_dissociation_score(
    df,
    rem_eeg_threshold=DISSOCIATION_REM_EEG_THRESHOLD,
    dissociation_threshold=DISSOCIATION_DELTA_THRESHOLD,
    emg_z_threshold=DISSOCIATION_EMG_Z_THRESHOLD,
):
    """
    Adds event-level REM-Wake dissociation metrics.

    Core idea:
        rem_wake_dissociation = P_REM_EEGonly - P_REM_FULL

    Interpretation:
        Positive values mean the EEG-only model looks more REM-like than the
        EEG+EMG/full model. If this happens together with high EMG, it is a
        candidate REM-with-motor-activation/RBD-like signature.
    """
    out = df.copy()

    p_rem_eeg_col = _first_existing_metric_column(
        out,
        [
            "P_REM_EEGonly",
            "mean_EEGonly_P_REM",
            "p_rem_eeg_only",
            "p_REM_eeg_only",
            "p_rem_eeg",
            "prob_rem_eeg_only",
        ],
    )

    p_rem_full_col = _first_existing_metric_column(
        out,
        [
            "P_REM_FULL",
            "mean_full_P_REM",
            "p_rem_eeg_emg",
            "p_REM_eeg_emg",
            "p_rem_full",
            "prob_rem_eeg_emg",
        ],
    )

    p_wake_eeg_col = _first_existing_metric_column(
        out,
        [
            "P_Wake_EEGonly",
            "P_Awake_EEGonly",
            "mean_EEGonly_P_Awake",
            "mean_EEGonly_P_Wake",
            "p_wake_eeg_only",
            "p_awake_eeg_only",
        ],
    )

    p_wake_full_col = _first_existing_metric_column(
        out,
        [
            "P_Wake_FULL",
            "P_Awake_FULL",
            "mean_full_P_Awake",
            "mean_full_P_Wake",
            "p_wake_eeg_emg",
            "p_awake_eeg_emg",
            "p_wake_full",
        ],
    )

    emg_col = _first_existing_metric_column(
        out,
        [
            "max_EMG_z",
            "max_EMG_baseline_z",
            "emg_z",
            "emg_rms_z",
            "emg_envelope_z",
        ],
    )

    # Main dissociation metric.
    if p_rem_eeg_col is not None and p_rem_full_col is not None:
        out["rem_wake_dissociation"] = (
            pd.to_numeric(out[p_rem_eeg_col], errors="coerce")
            - pd.to_numeric(out[p_rem_full_col], errors="coerce")
        )
    elif "delta_REM" in out.columns:
        out["rem_wake_dissociation"] = pd.to_numeric(out["delta_REM"], errors="coerce")
    else:
        out["rem_wake_dissociation"] = np.nan

    out["rem_wake_dissociation_pos"] = out["rem_wake_dissociation"].clip(lower=0)

    # Optional Wake-pull metric.
    if p_wake_eeg_col is not None and p_wake_full_col is not None:
        out["wake_pull_by_emg"] = (
            pd.to_numeric(out[p_wake_full_col], errors="coerce")
            - pd.to_numeric(out[p_wake_eeg_col], errors="coerce")
        )
        out["rem_wake_conflict_score"] = (
            out["rem_wake_dissociation_pos"] + out["wake_pull_by_emg"].clip(lower=0)
        ) / 2
    else:
        out["wake_pull_by_emg"] = np.nan
        out["rem_wake_conflict_score"] = out["rem_wake_dissociation_pos"]

    # EMG-weighted RBD-like score.
    if emg_col is not None:
        emg_positive = pd.to_numeric(out[emg_col], errors="coerce").clip(lower=0)
        out["rbd_dissociation_score"] = out["rem_wake_dissociation_pos"] * emg_positive
        emg_high = pd.to_numeric(out[emg_col], errors="coerce") >= emg_z_threshold
    else:
        out["rbd_dissociation_score"] = out["rem_wake_dissociation_pos"]
        emg_high = True

    if p_rem_eeg_col is not None:
        eeg_rem_like = pd.to_numeric(out[p_rem_eeg_col], errors="coerce") >= rem_eeg_threshold
    else:
        eeg_rem_like = True

    out["candidate_rbd_like_dissociation"] = (
        eeg_rem_like
        & (out["rem_wake_dissociation"] >= dissociation_threshold)
        & emg_high
    ).fillna(False)

    # A combined priority score you can sort by.
    if "rbd_priority_score" in out.columns:
        out["rbd_priority_score_plus_dissociation"] = (
            pd.to_numeric(out["rbd_priority_score"], errors="coerce").fillna(0)
            + out["rbd_dissociation_score"].fillna(0)
        )
    else:
        out["rbd_priority_score_plus_dissociation"] = out["rbd_dissociation_score"]

    return out


